public class OperasiAritmatika {
    public static void main (String[] args) {
        
        
        int a = 100;
        int b = 10;
        
        int hasil = a+b;
        System.out.println(hasil);
        
        System.out.println(a * b); 
        
        int c = 0;
        
        int result = c += 10;
        System.out.println(c);
        
        int angka = -19; //negatif
        int angka2 = +19; //positif
        
        //increment
        System.out.println(angka2++);
        System.out.println(++angka2);
        
        //decrement
        System.out.println(angka2--);
        System.out.println(--angka2);
    }
}
